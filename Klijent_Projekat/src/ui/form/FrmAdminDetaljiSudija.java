    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        // TODO add your handling code here:
        try {
            Sudija s = (Sudija) GUIKordinator.getInstance().getMap().get("izabrani_sudija");
            String korisnickoIme = txtKorisnickoIme.getText();
            String lozinka = txtLozinka.getText();
            String ime = txtIme.getText();
            String prezime = txtPrezime.getText();
            String mail = txtMail.getText();
            String profesija = txtProfesija.getText();
            String zemlja = txtZemlja.getText();
            Sudija sudija = new Sudija(s.getSudijaID(), korisnickoIme, lozinka, ime, prezime, mail, profesija, zemlja);
            Sudija sacuvaniSudija = KomunikacijaSaServerom.getInstanca().updateSudija(sudija);
            System.out.println(sacuvaniSudija);

            JOptionPane.showMessageDialog(this, "Sistem je izmenio sudiju sudiju!", "Uspesno", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da izmeni sudiju!", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

