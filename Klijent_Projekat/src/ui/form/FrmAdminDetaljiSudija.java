    private void pripremiFormu(ModFormeDetalji mod) throws Exception {
        setLocationRelativeTo(null);
        switch (mod) {
            case EDIT:
                prepareEdit();
                break;
            case SHOW:
                prepareShow();
                break;
            default:
                break;
        }
    }

    private void prepareEdit() throws Exception {
        Sudija s = (Sudija) GUIKordinator.getInstance().getMap().get("izabrani_sudija");
        List<Hakaton> hakatoni = KomunikacijaSaServerom.getInstanca().vratiHakatoneZaSudiju(s);
        napuniPodatke(s, hakatoni);
        srediZaEdit();
    }

    private void prepareShow() throws Exception {
        Sudija s = (Sudija) GUIKordinator.getInstance().getMap().get("izabrani_sudija");
        List<Hakaton> hakatoni = KomunikacijaSaServerom.getInstanca().vratiHakatoneZaSudiju(s);
        napuniPodatke(s, hakatoni);
        srediZaShow();

    }

    private void napuniPodatke(Sudija s, List<Hakaton> hakatoni) {
        tblHakatoni.setModel(new HakatoniTableModel(hakatoni));

        txtIme.setText(s.getIme());
        txtPrezime.setText(s.getPrezime());
        txtKorisnickoIme.setText(s.getKorisnickoIme());
        txtLozinka.setText(s.getLozinka());
        txtMail.setText(s.getMail());
        txtProfesija.setText(s.getProfesija());
        txtZemlja.setText(s.getZemlja());
    }

    private void srediZaShow() {
        tblHakatoni.setEnabled(false);
        btnIzmeni.setEnabled(false);

        txtIme.setEditable(false);
        txtZemlja.setEditable(false);
        txtKorisnickoIme.setEditable(false);
        txtMail.setEditable(false);
        txtPrezime.setEditable(false);
        txtLozinka.setEditable(false);
        txtProfesija.setEditable(false);

    }

    private void srediZaEdit() {
        tblHakatoni.setEnabled(false);
        btnIzmeni.setEnabled(true);

        txtIme.setEditable(true);
        txtZemlja.setEditable(true);
        txtKorisnickoIme.setEditable(true);
        txtMail.setEditable(true);
        txtPrezime.setEditable(true);
        txtLozinka.setEditable(true);
        txtProfesija.setEditable(true);
    }
