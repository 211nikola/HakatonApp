    private void preparePanel() throws Exception {
        //lblPrijavljeniAdmin.setText("Prijavljeni administrator : " + GUIKordinator.getInstance().getMap().get("active_user"));
        srediTabelu();
    }

    private void srediTabelu() throws Exception {
        String s = txtFilter.getText();
        List<Hakaton> listaHakatona = KomunikacijaSaServerom.getInstanca().vratiHakatone(s);
        tblPregled.setModel(new HakatoniTableModel(listaHakatona));

    }

