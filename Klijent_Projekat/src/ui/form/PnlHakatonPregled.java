    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed
        // TODO add your handling code here:
        try {

            if (validacijaTabele()) {
                HakatoniTableModel htm = (HakatoniTableModel) tblPregled.getModel();
                Hakaton h = htm.getHakaton(tblPregled.getSelectedRow());
                FrmAdminDetaljiHakaton fDetalji = new FrmAdminDetaljiHakaton((FrmAdminGlavna) SwingUtilities.getWindowAncestor(this), true, h);
                fDetalji.setVisible(true);
            } else {

                JOptionPane.showMessageDialog(this, "Izaberite hakaton!", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
                HakatoniTableModel htm = (HakatoniTableModel) tblPregled.getModel();
                List<Hakaton> lista = htm.getLista();
                if (!lista.isEmpty()) {
                    tblPregled.setRowSelectionInterval(0, 0);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Sistem ne moze da prikaze detalje o hakatonu.", "Greska", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_btnDetaljiActionPerformed

    private void btnPretragaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretragaActionPerformed
        // TODO add your handling code here:

        if (validacija()) {
            try {
                String s = txtFilter.getText();
                List<Hakaton> lista = KomunikacijaSaServerom.getInstanca().vratiHakatone(s);
                tblPregled.setModel(new HakatoniTableModel(lista));

                izbrisi();
                //JOptionPane.showMessageDialog(this, "Sistem ne moze da izvrsi pretragu.", "Greska", JOptionPane.ERROR_MESSAGE);

                //isprazni();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne moze da izvrsi pretragu.", "Greska", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(PnlHakatonPregled.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnPretragaActionPerformed

